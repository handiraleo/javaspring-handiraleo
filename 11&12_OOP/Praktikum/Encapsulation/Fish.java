
public class Fish {
    private String type;
    private String feed;

    public Fish (String type, String feed) {
        this.type = type;
        this.feed = feed;
    }
    
    public String getType() {
        return type;
    }
    public void setType(String type) {
        this.type = type;
    }
    public String getFeed() {
        return feed;
    }
    public void setFeed(String feed) {
        this.feed = feed;
    }
}





